func TestEmbedBinding(t *testing.T) {
	a, mock := testAgent(t)
	run := newRun(a, "run-1", "", "", context.Background())

	out, err := executeJS(run.vmRuntime(), `embed(["hello", "world"])`)
	if err != nil {
		t.Fatal(err)
	}
	// Mock returns one embedding per call regardless of input length, so we
	// just check the shape — outer array, inner numbers.
	if !strings.Contains(out, "0.1") || !strings.Contains(out, "0.2") {
		t.Fatalf("expected embedding values in result, got %s", out)
	}

	reqs := mock.RequestsByPath("/api/agent/llm/embedding")
	if len(reqs) != 1 {
		t.Fatalf("expected 1 embedding request, got %d", len(reqs))
	}
	var body struct {
		Capability string          `json:"capability"`
		Options    json.RawMessage `json:"options"`
	}
	json.Unmarshal(reqs[0].Body, &body)
	if body.Capability != "embedding" {
		t.Errorf("capability = %q, want embedding", body.Capability)
	}
	var opts struct {
		Values []string `json:"values"`
	}
	json.Unmarshal(body.Options, &opts)
	if len(opts.Values) != 2 || opts.Values[0] != "hello" || opts.Values[1] != "world" {
		t.Errorf("values = %v, want [hello world]", opts.Values)
	}
}

func TestEmbedAcceptsString(t *testing.T) {
	a, mock := testAgent(t)
	run := newRun(a, "run-1", "", "", context.Background())

	if _, err := executeJS(run.vmRuntime(), `embed("just one")`); err != nil {
		t.Fatal(err)
	}
	reqs := mock.RequestsByPath("/api/agent/llm/embedding")
	if len(reqs) != 1 {
		t.Fatalf("expected 1 request, got %d", len(reqs))
	}
	var body struct {
		Options json.RawMessage `json:"options"`
	}
	json.Unmarshal(reqs[0].Body, &body)
	var opts struct {
		Values []string `json:"values"`
	}
	json.Unmarshal(body.Options, &opts)
	if len(opts.Values) != 1 || opts.Values[0] != "just one" {
		t.Errorf("values = %v, want [just one]", opts.Values)
	}
}

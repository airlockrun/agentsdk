// Media bindings register on every VM. Each test verifies that the JS call
// produces the right wire requests against the mock Airlock — the actual
// transcription/generation is exercised by provider_test.go.

func TestTranscribeAudioBinding(t *testing.T) {
	a, mock := testAgent(t)
	a.RegisterStorage(&Storage{Slug: "uploads", Access: AccessUser})
	run := newRun(a, "run-1", "", "", context.Background())

	out, err := executeJS(run.vmRuntime(), `transcribeAudio("uploads/voice.ogg", { language: "en", prompt: "meeting" })`)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(out, `"text": "mock transcription"`) {
		t.Fatalf("expected text in result, got %s", out)
	}

	// Storage GET pulls the bytes from the agent's S3 bucket.
	if got := mock.RequestsByPath("/api/agent/storage/uploads/voice.ogg"); len(got) == 0 {
		t.Fatal("expected GET to /api/agent/storage/uploads/voice.ogg")
	}

	// Then transcription proxy is invoked with the bytes inline.
	reqs := mock.RequestsByPath("/api/agent/llm/transcription")
	if len(reqs) != 1 {
		t.Fatalf("expected 1 transcription request, got %d", len(reqs))
	}
	var body struct {
		Capability string          `json:"capability"`
		Slug       string          `json:"slug"`
		Options    json.RawMessage `json:"options"`
	}
	if err := json.Unmarshal(reqs[0].Body, &body); err != nil {
		t.Fatal(err)
	}
	if body.Capability != "transcription" {
		t.Errorf("capability = %q, want transcription", body.Capability)
	}
	if body.Slug != "" {
		t.Errorf("slug should be empty for capability-routed call, got %q", body.Slug)
	}
	var opts struct {
		Audio    []byte `json:"audio"`
		Language string `json:"language"`
		Prompt   string `json:"prompt"`
	}
	if err := json.Unmarshal(body.Options, &opts); err != nil {
		t.Fatal(err)
	}
	if string(opts.Audio) != "mock-file-content" {
		t.Errorf("audio = %q, want bytes from storage GET", string(opts.Audio))
	}
	if opts.Language != "en" {
		t.Errorf("language = %q, want en", opts.Language)
	}
	if opts.Prompt != "meeting" {
		t.Errorf("prompt = %q, want meeting", opts.Prompt)
	}
}

func TestGenerateImageBinding(t *testing.T) {
	a, mock := testAgent(t)
	a.RegisterStorage(&Storage{Slug: "uploads", Access: AccessUser})
	run := newRun(a, "run-1", "", "", context.Background())

	out, err := executeJS(run.vmRuntime(), `generateImage("a sunset", { saveAs: "uploads/out.png", size: "1024x1024" })`)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(out, `"key": "uploads/out.png"`) {
		t.Fatalf("expected key in result, got %s", out)
	}
	if !strings.Contains(out, `"mimeType": "image/png"`) {
		t.Fatalf("expected mimeType in result, got %s", out)
	}

	// Image proxy invocation.
	reqs := mock.RequestsByPath("/api/agent/llm/image")
	if len(reqs) != 1 {
		t.Fatalf("expected 1 image request, got %d", len(reqs))
	}
	var body struct {
		Capability string          `json:"capability"`
		Options    json.RawMessage `json:"options"`
	}
	json.Unmarshal(reqs[0].Body, &body)
	if body.Capability != "image" {
		t.Errorf("capability = %q, want image", body.Capability)
	}

	// Generated bytes were written back to agent storage at saveAs (zone-prefixed).
	if got := mock.RequestsByPath("/api/agent/storage/uploads/out.png"); len(got) == 0 {
		t.Fatal("expected PUT to /api/agent/storage/uploads/out.png")
	}
}

